export const formatPrice = (property: Property): string => {
  const price = property.type === 'rent' ? (property.rent || property.price) : property.price;
  if (property.type === 'rent') {
    const man = price / 10000;
    return `${man.toFixed(man % 1 === 0 ? 0 : 1)}万円/月`;
  }
  if (price >= 100000000) {
    return `${(price / 100000000).toFixed(1)}億円`;
  }
  if (price >= 10000) {
    const man = price / 10000;
    return `${man % 1 === 0 ? man : man.toFixed(1)}万円`;
  }
  return `${price.toLocaleString()}円`;
};

export const getPropertyTypeLabel = (type: string): string => {
  const labels: Record<string, string> = {
    apartment: 'マンション・アパート',
    house: '一戸建て',
    office: '店舗・事務所',
    land: '土地',
  };
  return labels[type] || type;
};

export const getTransactionTypeLabel = (type: string): string => {
  return type === 'rent' ? '賃貸' : '売買';
};
